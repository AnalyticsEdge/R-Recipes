# Create an object of 5 dates called dates starting at "2016-01-01".

dates <- as.Date('2018-01-01' + 0:4) 
# "2018-01-01" "2018-01-02" "2018-01-03" "2018-01-04" "2018-01-05"


dates <- as.Date('2018-01-01' + 1:4) 
# "2018-01-02" "2018-01-03" "2018-01-04" "2018-01-05"


dates <- seq(as.Date("2018-01-01"), length = 5, by = "days")
# "2018-01-01" "2018-01-02" "2018-01-03" "2018-01-04" "2018-01-05"
